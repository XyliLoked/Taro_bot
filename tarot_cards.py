    def get_card_by_name(self, name):
        for card in self.cards:
            if card.name.lower() == name.lower():
                return card
        return None
    
    def get_random_card(self):
        import random
        return random.choice(self.cards)
    
    def shuffle(self):
        import random
        random.shuffle(self.cards)
        return self.cards